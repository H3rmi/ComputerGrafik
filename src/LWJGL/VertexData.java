public class VertexData {
    public VertexData(float red, float green, float blue, float x, float y, float z) {
        this.red = red;
        this.green = green;
        this.blue = blue;
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float red ;
    public float green ;
    public float blue ;
    public float x;
    public float y;
    private float z;
}
